    public void setinformacion(ArrayList<Registro> reportes)
    {
            int acumulador=0;
        
            for(int i=0; i<reportes.size(); i++)
            {
                
                
                for(int j=0; j<reportes.get(i).getProducto().size(); j++)
                {
                    
                    int total = reportes.get(i).getproducto(j).getpreciocosto()*reportes.get(i).getproducto(j).getCantidad();
                    
                    jTable1.getModel().setValueAt(reportes.get(i).getproducto(j).getfecha(), i, 0);
                    jTable1.getModel().setValueAt(reportes.get(i).getproducto(j).getpreciocosto(), i, 2);
                    
                    if(reportes.get(i).getE_S()==false)
                    {
                        
                        jTable1.getModel().setValueAt("Salida"+" ("+reportes.get(i).getproducto(j).getnombre3()+")", i, 1);
                        jTable1.getModel().setValueAt(reportes.get(i).getproducto(j).getCantidad(), i, 5);
                        jTable1.getModel().setValueAt(total, i, 6);
                        
                        
                        
                        acumulador = acumulador-total;
                        
                        
                    }
                    else
                    {
                        
                        jTable1.getModel().setValueAt("Entrada"+" ("+reportes.get(i).getproducto(j).getnombre3()+")", i, 1);
                        jTable1.getModel().setValueAt(reportes.get(i).getproducto(j).getCantidad(), i, 3);
                        jTable1.getModel().setValueAt(total, i, 4);
                        
                        acumulador = acumulador+total;
                        
                    }
                    
                    if(i==reportes.size()-1 && j==reportes.get(reportes.size()-1).getProducto().size()-1)
                    {
                        
                        jTable1.getModel().setValueAt(acumulador, i+1, 8);
                    }
                    
                    
                }
            }
        
       
        
    }

